    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Dependente dep = new Dependente();
        Associado as = new Associado();

        as.setId(Integer.parseInt(JTFCodigoResponsavel.getText()));

        dep.setCpf(JTFCpfDep.getText());
        dep.setDataNascimento(FormatFactory.convertToDate(JTFDataNascimentoDep.getText()));
        dep.setEndereco(JTFEndereco.getText());
        dep.setNome(JTFNomeDep.getText());
        dep.setResponsavel(as);
        dep.setRg(JTFRgDep.getText());
        dep.setTelefone(JTFTelefoneDep.getText());
        dep.setIdade(Integer.parseInt(JTFIdadeDep.getText()));
        dep.setIsCasado(RadioButonIsCasado.isSelected());

        if (RadioBtnFilho.isSelected()) {
            dep.setParente(EnumParentesco.filho);
        }
        if (RadioBtnMarido.isSelected()) {
            dep.setParente(EnumParentesco.marido);
        }
        if (RadioBtnEsposa.isSelected()) {
            dep.setParente(EnumParentesco.esposa);
        }

        try {

            if (dep.ehMaiorDeIdade() && dep.isIsCasado() && RadioBtnFilho.isSelected()) {
                JOptionPane.showMessageDialog(null, "Não é possível cadastrar como dependente, será necessário cadastrar como Associado.");
            } else {

                if (JTFCodigo.getText().isEmpty() == false) {

                    dep.setId(Integer.parseInt(JTFCodigo.getText()));
                    new DependenteDAO().alterar(dep);

                } else if (JTFCodigo.getText().isEmpty() == true) {
                    new DependenteDAO().inserir(dep);
                }
            }
            setVisible(false);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar, verifique as informações");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro geral,contatar o suporte ");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

