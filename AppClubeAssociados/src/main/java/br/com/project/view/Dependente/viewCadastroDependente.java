    public void alterarRegistro(Dependente dep) {
        
        JTFCodigo.setText(String.valueOf(dep.getId()));
        JTFCodigoResponsavel.setText(String.valueOf(dep.getResponsavel().getId()));
        JTFCpfDep.setText(dep.getCpf());
        JTFDataNascimentoDep.setText(FormatFactory.formatDate(dep.getDataNascimento()));
        JTFEndereco.setText(dep.getEndereco());
        JTFIdadeDep.setText(String.valueOf(dep.getIdade()));
        JTFNomeDep.setText(dep.getNome());
        JTFRgDep.setText(dep.getRg());
        JTFTelefoneDep.setText(dep.getTelefone());

        if (dep.getParente().equals(EnumParentesco.esposa)) {
            RadioBtnEsposa.setSelected(true);
        } else if (dep.getParente().equals(EnumParentesco.marido)) {
            RadioBtnMarido.setSelected(true);
        } else if (dep.getParente().equals(EnumParentesco.filho)) {
            RadioBtnFilho.setSelected(true);
        }

        radioBtnNaoECasado.setSelected(true);

    }
