    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        final int tabela = jTableClube.getSelectedRow();

        if (tabela < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um Clube!");
        } else {

            final int id = (int) jTableClube.getValueAt(tabela, 0);

            try {

                Clube cl = new ClubeDAO().buscarPelaChave(id);

                viewCadastroClube vca = new viewCadastroClube(null, true);

                vca.alterarRegistro(cl);

                vca.setVisible(true);

                preencheGrid(new ClubeDAO().buscarTodos());

            } catch (SQLException ex) {
                JOptionPane.showConfirmDialog(null, "Erro Driver não encontrado ! ");
            } catch (ClassNotFoundException ex) {
                JOptionPane.showConfirmDialog(null, "Erro na conexão com o Bancou ou na inserção de dados.");
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        final int tabela = jTableClube.getSelectedRow();

        if (tabela < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma Clube!");
        } else {

            String nome = (String) jTableClube.getValueAt(tabela, 1);
            final int id = (int) jTableClube.getValueAt(tabela, 0);

            if (JOptionPane.showConfirmDialog(null, "Deseja excluir cadastro de " + nome + "?") == JOptionPane.YES_OPTION) {

                try {
                    Clube cl = new Clube();
                    cl.setId(id);
                    new ClubeDAO().apagar(cl);

                    preencheGrid(new ClubeDAO().buscarTodos());

                } catch (SQLException ex) {
                    JOptionPane.showConfirmDialog(null, "Erro Driver não encontrado !");
                } catch (ClassNotFoundException ex) {
                    JOptionPane.showConfirmDialog(null, "Erro na conexão com o Banco ou na inserção de dados.");
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

