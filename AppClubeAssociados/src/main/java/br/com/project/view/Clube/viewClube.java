     public void preencheGrid(ArrayList<Clube> lista) {
        
        Object[] columnNames = {"ID", "NOME DO CLUBE", "CNPJ", "LOCALIDADE", "RESPONSAVEL"};
        
        DefaultTableModel dtm = new DefaultTableModel(columnNames, 0);
        
        for (Clube cl : lista) {
            Object[] values = {cl.getId(), cl.getNome(), cl.getCnpj(), cl.getLocalidade(), cl.getResponsavel()};
            dtm.addRow(values);
        }
        
        getjTableClube().setModel(dtm);
    }
    
    
