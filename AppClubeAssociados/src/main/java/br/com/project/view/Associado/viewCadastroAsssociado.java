    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Associado associado = new Associado();
        Clube clube = new Clube();
        Mensalidade mensalidade = new Mensalidade();
        
        mensalidade.setValor(Double.parseDouble(JTFMensalidade.getText()));
        clube.setId(Integer.parseInt(JTFCodigoClube.getText()));
        associado.setAgencia(Integer.parseInt(JTFAgenciaAssociado.getText()));
        associado.setBanco(JTFBancoAssociado.getText());
        associado.setConta(Integer.parseInt(JTFContaAssociado.getText()));
        associado.setClubeParticipante(clube);
        associado.setCpf(JTFCpfAssociado.getText());
        associado.setDataNascimento(FormatFactory.convertToDate(JTFDateNascimento.getText()));
        associado.setEndereco(JTFEndereco.getText());
        associado.setNome(JTFNomeAssociado.getText());
        associado.setTelefone(JTFTelefoneAssociado.getText());
        associado.setRg(JTFRgAssociado.getText());
        associado.setMensalidade(mensalidade);
        
        try {
            
             if (JTFCodigoAssociado.getText().isEmpty() == false) {
                associado.setId(Integer.parseInt(JTFCodigoAssociado.getText()));
                new AssociadoDAO().alterar(associado);
            } 
            else if(JTFCodigoAssociado.getText().isEmpty() == true) {
                new AssociadoDAO().inserir(associado);
            }
             
            setVisible(false);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar, verifique as informa√ß√µes");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro geral,contatar o suporte " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

